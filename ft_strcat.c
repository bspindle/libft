#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	size_t i;
	size_t j;

	i = ft_strlen(s1);
	j = 0;
	while (j <= ft_strlen(s2))
	{
		s1[i + j] = s2[j];
		j++;
	}
	return (s1);
}
