#include "libft.h"

void	ft_del_chartab(char **tab)
{
	int i;

	i = 0;
	if (tab)
	{
		while (tab[i])
			i++;
		while (i >= 0)
		{
			free(tab[i]);
			i--;
		}
		free(tab);
	}
}
