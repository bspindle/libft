#include "libft.h"

void		ft_putnbr(int n)
{
	int		sign;
	char	str[15];
	int		i;

	i = 0;
	sign = 1;
	if (n == 0)
		write(1, "0", 1);
	if (n < 0)
	{
		sign = -1;
		write(1, "-", 1);
	}
	while (n != 0)
	{
		i++;
		str[i] = (n % (sign * 10)) * sign + 48;
		n = n / 10;
	}
	while (i > 0)
	{
		ft_putchar(str[i]);
		i--;
	}
}
